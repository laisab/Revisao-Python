"""
Vamos simular uma contagem regressiva de lançamento de foguete usando while.

1 - Crie uma variável começando em 10.
2 - Enquanto a contagem for maior ou igual a 0:
*   Imprima o valor da contagem.
3 - Quando o loop terminar, imprima: "Fogo!"
"""
contagem = 10

while contagem >= 0:
    print(contagem)
    contagem -= 1

print('Fogo!')
